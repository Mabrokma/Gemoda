/*
 *  This file contains a number of scoring matrices, most of which
 *  are intended for comparing amino acid sequences; however a few
 *  are for DNA.  In general, if a user wants to add their own 
 *  matrix for use with Gemoda, they should add it to this file
 *  and recompile Gemoda.
 *
 *  Note that users are not restricted to 23x23 matrices.  By changing
 *  aaOrder, you can easily make matrices for comparing ANSII strings
 *  with up to 256 different characters.
 *
 *  All of the matrices below were obtained directly from BLAST/WU-BLAST;
 *  they are all also part of the public domain, so there is nothing
 *  intrinsic to BLAST with respect to the matrices.  It was just the easiest
 *  way to get all of the matrices into our software.
 *
 *  The most popular matrix for amino acid sequences is blosum62.
 *
 *  A good location for getting new scoring matrices, such as those
 *  based on structural data, is the AAIndex.  URLs tend to change, so
 *  rather than us listing it here, Google it!
 *
 */

